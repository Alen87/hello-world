package oop_BillBurgers;

public class Hamburger {
	
	private String name;
	private String meat;
	private String breadRollType;
	private Double price;
	
	
	public Hamburger(String name, String meat, String breadRollType, Double price) {
		super();
		this.name = name;
		this.meat = meat;
		this.breadRollType = breadRollType;
		this.price = price;
	}

	
	
	
	
}
